pub fn resample(
    output: &mut [i32],
    input: &[i16],
    sample_rate_ratio: FixedPoint<49, 15>,
    fraction: &mut FixedPoint<49, 15>,
    samples_to_write: u32,
    src_quality: SrcQuality,
) {
    match src_quality {
        SrcQuality::Low => {
            resample_low_quality(output, input, sample_rate_ratio, fraction, samples_to_write)
        }
        SrcQuality::Medium => {
            resample_normal_quality(output, input, sample_rate_ratio, fraction, samples_to_write)
        }
        SrcQuality::High => {
            resample_high_quality(output, input, sample_rate_ratio, fraction, samples_to_write)
        }
    }
}

pub fn resample_low_quality(
    output: &mut [i32],
    input: &[i16],
    sample_rate_ratio: FixedPoint<49, 15>,
    fraction: &mut FixedPoint<49, 15>,
    samples_to_write: u32,
) {
    let samples_to_write = samples_to_write.min(output.len() as u32) as usize;
    if sample_rate_ratio == FixedPoint::from_f32(1.0) {
        for i in 0..samples_to_write {
            output[i] = input[i] as i32;
        }
    } else {
        let mut read_index = 0usize;
        for i in 0..samples_to_write {
            output[i] =
                input[read_index + usize::from(*fraction >= FixedPoint::from_f32(0.5))] as i32;
            *fraction += sample_rate_ratio;
            read_index += fraction.to_int_floor() as usize;
            fraction.clear_int();
        }
    }
}

fn normal_quality_lut(sample_rate_ratio: FixedPoint<49, 15>) -> &'static [f32; 512] {
    if sample_rate_ratio <= FixedPoint::from_f32(1.0) {
        &RESAMPLE_NORMAL_QUALITY_LUT2
    } else if sample_rate_ratio < FixedPoint::from_f32(1.3) {
        &RESAMPLE_NORMAL_QUALITY_LUT1
    } else {
        &RESAMPLE_NORMAL_QUALITY_LUT0
    }
}

pub fn resample_normal_quality(
    output: &mut [i32],
    input: &[i16],
    sample_rate_ratio: FixedPoint<49, 15>,
    fraction: &mut FixedPoint<49, 15>,
    samples_to_write: u32,
) {
    let samples_to_write = samples_to_write.min(output.len() as u32) as usize;
    let lut = normal_quality_lut(sample_rate_ratio);
    let mut read_index = 0usize;
    for i in 0..samples_to_write {
        let lut_index = ((fraction.get_frac() >> 8) as usize) * 4;
        let sample0 =
            FixedPoint::<56, 8>::from_f32(input[read_index + 0] as f32 * lut[lut_index + 0]);
        let sample1 =
            FixedPoint::<56, 8>::from_f32(input[read_index + 1] as f32 * lut[lut_index + 1]);
        let sample2 =
            FixedPoint::<56, 8>::from_f32(input[read_index + 2] as f32 * lut[lut_index + 2]);
        let sample3 =
            FixedPoint::<56, 8>::from_f32(input[read_index + 3] as f32 * lut[lut_index + 3]);
        output[i] = (sample0 + sample1 + sample2 + sample3).to_int_floor();
        *fraction += sample_rate_ratio;
        read_index += fraction.to_int_floor() as usize;
        fraction.clear_int();
    }
}

