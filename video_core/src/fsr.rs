/// Convert f32 to half-float u16, returned as u32 (matching AU1_AH1_AF1).
fn au1_ah1_af1(f: f32) -> u32 {
    let u = f.to_bits();
    let i = (u >> 23) as usize;
    BASE[i] + ((u & 0x7fffff) >> SHIFT[i] as u32)
}

/// Pack two half-floats into a single u32 (matching AU1_AH2_AF2).
fn au1_ah2_af2(a: [f32; 2]) -> u32 {
    au1_ah1_af1(a[0]) + (au1_ah1_af1(a[1]) << 16)
}

fn fsr_easu_con(
    con0: &mut [u32; 4],
    con1: &mut [u32; 4],
    con2: &mut [u32; 4],
    con3: &mut [u32; 4],
    input_viewport_x: f32,
    input_viewport_y: f32,
    input_size_x: f32,
    input_size_y: f32,
    output_size_x: f32,
    output_size_y: f32,
) {
    con0[0] = (input_viewport_x / output_size_x).to_bits();
    con0[1] = (input_viewport_y / output_size_y).to_bits();
    con0[2] = (0.5 * input_viewport_x / output_size_x - 0.5).to_bits();
    con0[3] = (0.5 * input_viewport_y / output_size_y - 0.5).to_bits();
    con1[0] = (1.0 / input_size_x).to_bits();
    con1[1] = (1.0 / input_size_y).to_bits();
    con1[2] = (1.0 / input_size_x).to_bits();
    con1[3] = (-1.0 / input_size_y).to_bits();
    con2[0] = (-1.0 / input_size_x).to_bits();
    con2[1] = (2.0 / input_size_y).to_bits();
    con2[2] = (1.0 / input_size_x).to_bits();
    con2[3] = (2.0 / input_size_y).to_bits();
    con3[0] = (0.0 / input_size_x).to_bits();
    con3[1] = (4.0 / input_size_y).to_bits();
    con3[2] = 0;
    con3[3] = 0;
}

/// Generate FSR EASU constants with pixel offset.
#[allow(clippy::too_many_arguments)]
pub fn fsr_easu_con_offset(
    con0: &mut [u32; 4],
    con1: &mut [u32; 4],
    con2: &mut [u32; 4],
    con3: &mut [u32; 4],
    input_viewport_x: f32,
    input_viewport_y: f32,
    input_size_x: f32,
    input_size_y: f32,
    output_size_x: f32,
    output_size_y: f32,
    input_offset_x: f32,
    input_offset_y: f32,
) {
    fsr_easu_con(
        con0,
        con1,
        con2,
        con3,
        input_viewport_x,
        input_viewport_y,
        input_size_x,
        input_size_y,
        output_size_x,
        output_size_y,
    );
    con0[2] = (0.5 * input_viewport_x / output_size_x - 0.5 + input_offset_x).to_bits();
    con0[3] = (0.5 * input_viewport_y / output_size_y - 0.5 + input_offset_y).to_bits();
}

/// Generate FSR RCAS (sharpening) constants.
pub fn fsr_rcas_con(con: &mut [u32; 4], sharpness: f32) {
    let sharpness = 2.0f32.powf(-sharpness);
    let h_sharp = [sharpness, sharpness];
    con[0] = sharpness.to_bits();
    con[1] = au1_ah2_af2(h_sharp);
    con[2] = 0;
    con[3] = 0;
}
